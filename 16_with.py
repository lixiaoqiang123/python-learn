# with 关键字
# with 关键字为我们提供了一种优雅的方式来处理文件操作、数据库连接等需要明确释放资源的场景。
# 它的核心作用是自动管理资源的生命周期，确保资源在使用完毕后被正确地清理和释放，即使在处理过程中发生错误。

# 传统方式
file = open("example.txt", "w")
try:
    file.write("Hello, World!")
finally:
    file.close()  # 必须手动关闭文件

# 使用 with 关键字
with open("example.txt", "r") as file:
    content = file.read()
    print(content)
# 文件在 with 块结束后自动关闭，无需手动调用 close()

# with 语句背后的魔法：上下文管理器
# with 语句依赖于上下文管理器（Context Manager）协议。一个对象要成为上下文管理器，
# 只需要实现 __enter__() 和 __exit__() 两个特殊方法。
# __enter__()：在进入 with 块之前执行，通常用于获取资源（如打开文件、建立数据库连接）。
# __exit__(exc_type, exc_val, exc_tb)：在退出 with 块时执行，用于清理资源（如关闭文件、断开连接）。
# 即使在 with 块内部发生异常，__exit__() 方法也会被调用，从而确保资源的正确释放。

# __exit__() 方法接收三个参数：
# exc_type：异常类型
# exc_val：异常值
# exc_tb：异常追踪信息
# 如果 __exit__() 返回 True，则表示异常已被处理，不会继续传播；返回 False 或 None，异常会继续向外传播。

# ------------------------------------------------------简单的上下文管理器示例
class Timer:
    def __enter__(self):
        import time
        self.start = time.time()
        return self
    
    def __exit__(self, exc_type, exc_val, exc_tb):
        import time
        self.end = time.time()
        print(f"耗时: {self.end - self.start:.2f}秒")
        return False

# 使用示例
with Timer() as t:
    # 执行一些耗时操作
    sum(range(1000000))

# -------------------------------------------------------使用 contextlib 模块
from contextlib import contextmanager

@contextmanager
def tag(name):
    # yield 之前的代码：就等同于类中的 __enter__() 方法。在这里你输出了开始标签 <h1>。
    print(f"<{name}>")
    # yield 语句本身：它代表将控制权暂时交给 with 代码块内部的代码（在这里就是执行 print("这是一个标题")）。
    # 如果你写了 yield 某个值，这个值就会被赋给 with ... as 变量: 中的那个变量。
    yield
    # yield 之后的代码：就等同于类中的 __exit__() 方法。当 with 块内部的代码执行完毕后，程序会回到这里继续执行，从而输出了结束标签 </h1>。
    print(f"</{name}>")

# 使用示例
with tag("h1"):
    print("这是一个标题")



