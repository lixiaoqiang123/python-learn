# 最佳实践
# 优先使用 with 管理资源：对于文件、网络连接、锁等资源，总是优先考虑使用 with 语句
# 保持上下文简洁：with 块中的代码应该只包含与资源相关的操作
# 合理处理异常：在自定义上下文管理器中，根据需求决定是否抑制异常
# 利用多个上下文：Python 允许在单个 with 语句中管理多个资源

# ----------------------------------------------------------生成器函数
# 普通函数的运行逻辑（一气呵成）：被调用后，从头跑到尾，遇到 return 就直接结束并退出。并且交出所有的局部变量内存。
# 生成器函数的运行逻辑（随时暂停与恢复）： 被调用时，它不会马上执行代码，而是返回一个生成器对象。 
# 当你使用 next() 催促它时，它才开始干活。一旦遇到 yield，它就会按下暂停键，吐出一个值给你，然后保持当前所有的状态（比如此时局部变量是什么值，正处于循环的第几次）。 下次你再催它，它会从上次暂停的地方接着继续干活，直到遇到下一个 yield 或者代码执行完毕。
