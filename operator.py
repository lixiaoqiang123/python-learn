#-----------------------------------------------------算数运算符
#取模，返回除法的余数  %
a = 10
b = 3
print(a % b)  # 1
print(b % a)  # 3 当 被除数 < 除数 时：被除数 % 除数 = 被除数
#幂 返回x的y次幂 **
x = 10
y = 2
print(x ** y) # 100 10的2次幂

#取整除 向下取整 //
z = 10
w = 3
print(z // w) # 3
print(w // z) # 0



#------------------------------------------------------比较运算符
# == != > < >= <=
# == 比较的是值，而不是内存地址 ，两个对象的内容是否相等
# is 比较内存地址，两个变量是否指向同一个对象
c = [1,2,3,4]
d = [1,2,3,4]
print(c == d) # True
print(c is d) # False
# 查看内存地址
print(id(a))    # 例如: 140234567890
print(id(b))    # 例如: 140234567950  (不同)

# Python 对 -5 ~ 256 的小整数有缓存，会复用同一对象：
print(a == x) # True

#------------------------------------------------------身份运算符
# is is not
# is 两个变量指向同一个对象，返回 True
# is not 两个变量不是同一个对象，返回 True
print(a is x) # True
# ✅ 判断是否为 None 用 is（None 是单例）
if x is None:
    pass



#------------------------------------------------------逻辑运算符
# 以下都被视为 False
# False, 0, 0.0, "", [], {}, (), None
# 其余都被视为 True（Truthy）
# True, 1, -1, "hello", [0], ...
# and or not
# and：找到第一个"假值"就返回它，否则返回最后一个值
print(a and b) #布尔"与" - 如果 x 为 False，x and y 返回 x 的值，否则返回 y 的计算值。
# or：找到第一个"真值"就返回它，否则返回最后一个值
print(a or b) #布尔"或" - 如果 x 是 True，它返回 x 的值，否则它返回 y 的计算值。
# not：始终返回 True 或 False
print(not a) #布尔"非" - 如果 x 为 True，返回 False 。如果 x 为 False，它返回 True。



#------------------------------------------------------赋值运算符
# = += -= *= /= //= %= **= &= |= ^= >>= <<=
# := 海象运算符，这个运算符的主要目的是在表达式中同时进行赋值和返回赋值的值。Python3.8 版本新增运算符。
# 传统写法（需要两行）
n = len(c)
if n > 3:
    print(f"List is too long ({n} elements)")

# 海象运算符（一行搞定）
if (n := len(c)) > 3:
    print(f"List is too long ({n} elements)")


