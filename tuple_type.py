# 元组
# ----------------------------------------------修改元组
# 元组是不可变的，所以不能修改元组中的元素
# 但是可以通过连接两个元组来创建一个新的元组
tup1 = (12, 34.56)
tup2 = ('abc', 'xyz')
tup3 = tup1 + tup2
print(tup3)  # 输出: (12, 34.56, 'abc', 'xyz')

# 删除元组
tup = ('Google', 'Runoob', 1997, 2000)
del tup
# print(tup)  # 这将引发 NameError，因为 tup 已经被删除了

# ----------------------------------------------元组运算符
# 计算元素个数
print(len(tup3))  # 输出: 4
# 连接，c 就是一个新的元组，它包含了 a 和 b 中的所有元素。
print(tup1 + tup2)  # 输出: (12, 34.56, 'abc', 'xyz')
# 复制
print(tup1 * 2)  # 输出: (12, 34.56, 12, 34.56)
# 元素是否存在
print(12 in tup1)  # 输出: True
print(100 not in tup1)  # 输出: True
# 迭代
for i in tup1:
    print(i, end=' ')  # 输出: 12 34.56


# ----------------------------------------------元组内置函数
# len(tuple) 计算元组元素个数
print(len(tup3))  # 输出: 4
# 当元组包含字母（字符串）或字符时，比较是基于字典序（lexicographical order），
# 即按字符的 Unicode 码点（ASCII 值）进行比较，而不是按字母表的“自然”顺序。
# 字符串比较：从左到右逐字符比较，直到找到不同的字符。字符的码点决定大小（例如，'a' 的码点是 97，'A' 是 65，所以 'A' < 'a'）。
# 字符：在 Python 中，字符就是长度为 1 的字符串，所以规则相同。
# 如果元组中元素类型不一致（例如混合数字和字符串），会抛出 TypeError，因为无法比较不同类型。
tup4 = (1, 'apple', 3.14)
print(max(tup4))  # 这将引发 TypeError，因为无法比较整数、字符串和浮点数
# max(tuple) 返回元组中元素最大值
print(max(tup1))  # 输出: 34.56
# min(tuple) 返回元组中元素最小值
print(min(tup1))  # 输出: 12
# tuple(iterable) 将可迭代系列转换为元组。
print(tuple([1, 2, 3]))  # 输出: (1, 2, 3)
# 查看内存地址
print(id(tup1))
